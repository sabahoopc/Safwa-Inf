function platformPills(links) {
  if (!links) return '';
  return Object.entries(links)
    .filter(([, url]) => url)
    .map(([key, url]) => `
      <a class="platform-pill" href="${url}" target="_blank" rel="noopener">
        ${ICONS[key] || ''}
        ${PLATFORM_LABELS[key] || key}
      </a>`)
    .join('');
}
